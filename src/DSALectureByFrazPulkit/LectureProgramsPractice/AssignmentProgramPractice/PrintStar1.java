package DSALectureByFrazPulkit.LectureProgramsPractice.AssignmentProgramPractice;
import java.util.Scanner;

public class PrintStar1 {
	public static void main(String[] arg)
	{
		int n = 5;
		for (int i =0; i < n; i++)
		{
			for (int j=0; j< n; j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}
}
